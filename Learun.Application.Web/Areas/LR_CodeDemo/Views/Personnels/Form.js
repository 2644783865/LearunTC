var acceptClick;
var keyValue = request('keyValue');
var bootstrap = function ($, learun) {
    "use strict";
    var page = {
        init: function () {
            $('.lr-form-wrap').lrscroll();
            page.bind();
            page.initData();
        },
        bind: function () {
            $('#F_ApplicantId').lrDataSourceSelect({ code: 'applicantdata', value: 'f_applicantid', text: 'f_companyname' });
            $('#F_SceneType').lrDataItemSelect({ code: 'SceneType' });
            $('#F_Gender').lrDataItemSelect({ code: 'Gender' });
            $("#F_IDCardNo").bind('input', function () {
                var age = learun.getIDCardAge($("#F_IDCardNo").val());
                $("#F_Age").val(age);
                var gender = learun.getIDCardGender($("#F_IDCardNo").val());
                $("#F_Gender").lrselectSet(gender);
            })


        },
        initData: function () {
            if (!!keyValue) {
                $.lrSetForm(top.$.rootUrl + '/LR_CodeDemo/Personnels/GetFormData?keyValue=' + keyValue, function (data) {
                    for (var id in data) {
                        if (!!data[id].length && data[id].length > 0) {
                            $('#' + id).jfGridSet('refreshdata', data[id]);
                        }
                        else {
                            $('[data-table="' + id + '"]').lrSetFormData(data[id]);
                        }
                    }
                    var gender = learun.getIDCardGender($("#F_IDCardNo").val());
                    $("#F_Gender").lrselectSet(gender);
                    var age = learun.getIDCardAge($("#F_IDCardNo").val());
                    $("#F_Age").val(age);
                });

                //setTimeout(function () {
                //    var gender = learun.getIDCardGender($("#F_IDCardNo").val());
                //    $("#F_Gender").lrselectSet(gender);
                //    var age = learun.getIDCardAge($("#F_IDCardNo").val());
                //    $("#F_Age").val(age);
                //}, 1000);
            }
        }
    };
    // 保存数据
    acceptClick = function (callBack) {
        if (!$('body').lrValidform()) {
            return false;
        }
        var postData = {
            strEntity: JSON.stringify($('body').lrGetFormData())
        };
        $.lrSaveForm(top.$.rootUrl + '/LR_CodeDemo/Personnels/SaveForm?keyValue=' + keyValue, postData, function (res) {
            // 保存成功后才回调
            if (!!callBack) {
                callBack();
            }
        });
    };
    page.init();
}
